/**
 * 检测是否应启用 DeepSeek 特化模式。
 * 由 apiConfig.useDeepSeekMode 手动开关控制。
 */
export function isDeepSeekMode(apiConfig?: { useDeepSeekMode?: boolean }): boolean {
    return apiConfig?.useDeepSeekMode === true;
}

